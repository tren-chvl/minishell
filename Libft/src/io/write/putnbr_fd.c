#include <libft_io.h>

void	putnbr_fd(ssize_t n, int fd)
{
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n > 9)
	{
		putnbr_fd(n / 10, fd);
		putnbr_fd(n % 10, fd);
		return ;
	}
	putchar_fd(n % 10 + '0', fd);
}
